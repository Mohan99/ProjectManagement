package com.nacre.formbean;

public class ProjectStatus {
	
	private String projectId;
	private String projectTitle;
	private String projectDescription;
	private String projectCompletionPercentage;
	private String expectedStartDate;
	private String expectedEndDate;
	private String statusInfo;
	private String updationDate;
	private String projectManagerName;
	private String projectManagerEmail;
	private String projectManagerMobileNo;
	private String gender;
	private String image;
	
	public String getImage() {
		return image;
	}
	public void setImage(String image) {
		this.image = image;
	}
	public String getProjectId() {
		return projectId;
	}
	public void setProjectId(String projectId) {
		this.projectId = projectId;
	}
	public String getProjectTitle() {
		return projectTitle;
	}
	public void setProjectTitle(String projectTitle) {
		this.projectTitle = projectTitle;
	}
	public String getProjectDescription() {
		return projectDescription;
	}
	public void setProjectDescription(String projectDescription) {
		this.projectDescription = projectDescription;
	}
	public String getProjectCompletionPercentage() {
		return projectCompletionPercentage;
	}
	public void setProjectCompletionPercentage(String projectCompletionPercentage) {
		this.projectCompletionPercentage = projectCompletionPercentage;
	}
	public String getExpectedStartDate() {
		return expectedStartDate;
	}
	public void setExpectedStartDate(String expectedStartDate) {
		this.expectedStartDate = expectedStartDate;
	}
	public String getExpectedEndDate() {
		return expectedEndDate;
	}
	public void setExpectedEndDate(String expectedEndDate) {
		this.expectedEndDate = expectedEndDate;
	}
	public String getStatusInfo() {
		return statusInfo;
	}
	public void setStatusInfo(String statusInfo) {
		this.statusInfo = statusInfo;
	}
	public String getUpdationDate() {
		return updationDate;
	}
	public void setUpdationDate(String updationDate) {
		this.updationDate = updationDate;
	}
	public String getProjectManagerName() {
		return projectManagerName;
	}
	public void setProjectManagerName(String projectManagerName) {
		this.projectManagerName = projectManagerName;
	}
	public String getProjectManagerEmail() {
		return projectManagerEmail;
	}
	public void setProjectManagerEmail(String projectManagerEmail) {
		this.projectManagerEmail = projectManagerEmail;
	}
	public String getProjectManagerMobileNo() {
		return projectManagerMobileNo;
	}
	public void setProjectManagerMobileNo(String projectManagerMobileNo) {
		this.projectManagerMobileNo = projectManagerMobileNo;
	}
	
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	

}
